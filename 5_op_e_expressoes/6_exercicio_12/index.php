<?php

  # Crie um arquivo PHP;
  # Crie uma operação com cada um dos básicos;
  # Cada operação deve estar em uma variável diferente;
  # Imprima cada uma das etapas;
  # EX:. soma -> multiplicação -> divisão -> subtração;

  $soma = 69 + 54; // 123
  echo $soma . "<br>";

  $multi = 18 * 6; // 108
  echo $multi . "<br>";

  $divisao = 20 / 2; // 10
  echo $divisao . "<br>";

  $subtracao = 58 - 20; //38
  echo $subtracao . "<br>";

  $op = $soma + $multi + $divisao * $subtracao; //611
  echo $op . "<br>";
